def eval_P_tilde(P0_tilde, beta = 1000, tau_h5="~/ir_grids/1e6_168.h5"):
    '''
    1) Transform P of tau to frequency axis;
    2) Solve Dyson equation;
    3) Transform back to tau axis.
    '''

    print("*****    P_tilde    *****")

    NQ = P0_tilde.shape0[-1]  # Number of aux points. 
    fourier = IR_factory(beta, tau_h5)
    # transform P0(tau) to P0(iw).
    P0_iw   = fourier.tauf_to_wb(P0_tilde)
    num_freq  = P0_iw.shape[0] # find out number of frequency points.
    num_s   = P0_iw.shape[1]  # Number of spins
    num_k   = P0_iw.shape[2]  # Number of k-points
    
    # solve Dyson eqn: P(iw) = [I-P0(iw)]^(-1)P0(iw)
    P_iw = np.zeros((num_freq,num_s,num_k,NQ,NQ), dtype=np.complex128)
    for iw in range(num_freq):
        for s in range(num_s):
            for k in range(num_k):
                idtt = np.identity(NQ, dtype=np.complex128)  # define identity matrix.
                temp = idtt - P0_iw[iw,s,k,:,:]
                temp = inv(temp)  # using inverse function from scipy. 
                temp = 0.5 * (temp + temp.conjugate().transpose())  # Hermitization
                P_iw[iw,s,k,:,:] += np.matmul(temp, P0_iw[iw,s,k,:,:])
            
    # transform P(iw) to P(tau).
    P_tilde = fourier.wb_to_tauf(P_iw)

    return P_tilde
