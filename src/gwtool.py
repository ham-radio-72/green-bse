def symmetrize_P0(P0_tilde):
    '''
        for (size_t it = t_start; it < t_end; ++it) { // Symmetry of tau: P0(t) = P0(beta - t)
      _P0_tilde(_nts - it - 1, 0).matrix() = _P0_tilde(it, 0).matrix();
    }
    '''
    print("Symmetrizing P0...")
    num_tau = P0_tilde.shape[0]
    num_s   = P0_tilde.shape[1]  # Number of spins
    num_k   = P0_tilde.shape[2]  # Number of k-points
    for t in range(num_tau//2):
        for s in range(num_s):
            for k in range(num_k):
                # Hermitization
                P0_tilde[t,s,k,:,:] = 0.5 * (P0_tilde[t,s,k,:,:] + P0_tilde[t,s,k,:,:].conjugate().transpose())
                tt = num_tau - t - 1
                P0_tilde[tt,s,k,:,:] = P0_tilde[t,s,k,:,:]

    return P0_tilde


